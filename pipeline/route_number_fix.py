"""엑셀 날짜 오염된 노선번호 복원.

배경: 원본 CSV를 엑셀로 열면 "3-1" 같은 노선번호를 날짜로 자동 인식해
"03월 01일" 형태로 오염시킨다. 역변환하면 "MM월 DD일" -> f"{int(MM)}-{int(DD)}".

실측 오염 값 (승하차 CSV의 고유 노선번호 32개):
  01월 13일, 01월 16일, 02월 05일, 03월 05일, 05월 01일~05월 15일,
  06월 13일, 07월 01일~07월 09일, 08월 01일, 08월 05일,
  10월 01일, 10월 05일, 11월 01일, 12월 01일, 12월 05일
노선정보 CSV에도 9개(07월 01~03일, 08/10/11/12월 01일, 01월 13/16일)가 겹친다.
모두 아래 일반 패턴으로 복원되므로 하드코딩 예외는 현재 불필요하다.
"""
import re

# "MM월 DD일" (공백 유무 허용)
_DATE_RE = re.compile(r"^\s*(\d{1,2})\s*월\s*(\d{1,2})\s*일\s*$")

# 일반 패턴으로 복원되지 않는 특수 케이스 대비 오버라이드 표(현재 비어 있음).
_OVERRIDES: dict[str, str] = {}


def fix_route_number(raw: str) -> str:
    """오염된 노선번호를 복원한다. 정상 값은 그대로 반환.

    "03월 01일" -> "3-1", "05월 15일" -> "5-15".
    근거 없는 추측을 하지 않는다: 날짜 패턴이 아니면 원본을 그대로 돌려준다.
    """
    if raw is None:
        return raw
    s = str(raw).strip()
    if s in _OVERRIDES:
        return _OVERRIDES[s]
    m = _DATE_RE.match(s)
    if m:
        month = int(m.group(1))
        day = int(m.group(2))
        return f"{month}-{day}"
    return s
